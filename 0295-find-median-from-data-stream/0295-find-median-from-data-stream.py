class MedianFinder:

    def __init__(self):
        self.max_heap = [] # so that the max element from the middle appears on top for easier access (here we maintain the left sorted part of the data stream)
        self.min_heap = [] # so that the min element from the middle appears on top for easier access (here we maintain thr right sorted part of the data stream)

    def addNum(self, num: int) -> None:
        heapq.heappush(self.max_heap, -num)
        heapq.heappush(self.min_heap, -heapq.heappop(self.max_heap))
        if len(self.min_heap) > len(self.max_heap):
            heapq.heappush(self.max_heap, -heapq.heappop(self.min_heap))

    def findMedian(self) -> float:
        if len(self.max_heap) > len(self.min_heap):
            return -self.max_heap[0]
        return (-self.max_heap[0] + self.min_heap[0])/2
        


